OUTLINE = (0x40, 0x40, 0x40, 0xFF)
TRANSPARENT = (0, 0, 0, 0)

# Wood colors
WOOD_HIGHLIGHT = (0xC4, 0xA4, 0x84)
WOOD_BASE = (0xA0, 0x84, 0x6C)
WOOD_SHADOW = (0x7C, 0x64, 0x50)

# Foliage colors  
FOLIAGE_HIGHLIGHT = (0x88, 0xCC, 0x50)
FOLIAGE_BASE = (0x64, 0xA8, 0x38)
FOLIAGE_SHADOW = (0x4C, 0x78, 0x28)

def add_outline(img):
    """Add 1px #404040 outline"""
    width, height = img.size
    pixels = img.load()
    
    opaque = set()
    for y in range(height):
        for x in range(width):
            if pixels[x, y][3] > 0:
                opaque.add((x, y))
    
    outline_pixels = set()
    for x, y in opaque:
        for dx in [-1, 0, 1]:
            for dy in [-1, 0, 1]:
                if dx == 0 and dy == 0:
                    continue
                nx, ny = x + dx, y + dy
                if 0 <= nx < width and 0 <= ny < height:
                    if (nx, ny) not in opaque:
                        outline_pixels.add((nx, ny))
    
    for x, y in outline_pixels:
        pixels[x, y] = OUTLINE
    
    return img

